public class Check_diff_int_ling {
    /*
Вычисляет количество кубических дюймов в кубической миле.
*/
    public static void main(String[] args) {
        int ci;
        int im;
        im = 2 * 12;
        ci = im * im * im;
        System.out.println("В кубической миле содержится " + ci +
                " кубических дюймов.");

        //1507852288
        //254358061056000
    }
}
