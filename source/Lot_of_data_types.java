public class Lot_of_data_types {
    public static void main(String[] args) {
        /*
         Тут, в этом разделе обозначаються инные типы даных
         int - целочислинные выражения
         double - выражения с плавающей точкой
         float -
         */

        int v;
        double x;
        float x_1;

        v = 10;
        x = 10.0;
        x_1 = 10;

        System.out.println("Primary announcement v: " + v);
        System.out.println("Primary announcement x: " + x);
        System.out.println("Primary announcement x_1 :" + x_1);
        System.out.println(); // Вывод пустой строки

        v = v/4;
        x = x/4;
        x_1 = x_1/4;

        System.out.println("v after divide: " + v);
        System.out.println("x after divide: " + x);
        System.out.println("x_1 after divide:" + x_1);

    }
}
